"use client";

import { clientConsentKeys } from "@/components/ai/ConsentCenter";
import { MessageCircle, Send, X } from "lucide-react";
import Link from "next/link";
import { usePathname } from "next/navigation";
import { useCallback, useEffect, useMemo, useState } from "react";

type Suggestion = {
  path: string;
  title: string;
  reason: string;
  confidence: number;
};

type ChatMessage = {
  id: string;
  role: "assistant" | "user";
  content: string;
  suggestions?: Suggestion[];
};

type ConsentState = {
  userId: string;
  analytics: boolean;
  training: boolean;
};

function createSessionId() {
  return `nav-${Date.now().toString(36)}-${Math.random().toString(36).slice(2, 8)}`;
}

const STARTER_PROMPTS = [
  "I am new. Where should I start?",
  "Take me to AI tools",
  "How do I find pricing and billing?",
  "I want trading intelligence",
];

const INTRO_COMPLETE_EVENT = "tradehax:intro-complete";

type IntroCompleteDetail = {
  source?: string;
  autoOpenNavigator?: boolean;
  prompt?: string;
  preferredMode?: string;
};

type NavigatorPreference = {
  hasInteracted: boolean;
  autoOpen: boolean;
};

const NAVIGATOR_PREFERENCE_KEY = "tradehax:navigator-preference";

export function SiteNavigatorWidget() {
  const pathname = usePathname();
  const [open, setOpen] = useState(false);
  const [priorityDock, setPriorityDock] = useState(false);
  const [navigatorPreference, setNavigatorPreference] = useState<NavigatorPreference>({
    hasInteracted: false,
    autoOpen: false,
  });
  const [pendingPrompt, setPendingPrompt] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);
  const [input, setInput] = useState("");
  const [sessionId] = useState(createSessionId);
  const [consent, setConsent] = useState<ConsentState>({
    userId: sessionId,
    analytics: true,
    training: false,
  });
  const [messages, setMessages] = useState<ChatMessage[]>([
    {
      id: "welcome",
      role: "assistant",
      content:
        "I can help you navigate TradeHax. Ask me what you want to do, and I’ll point you to the right pages.",
    },
  ]);

  const canSend = useMemo(() => input.trim().length > 0 && !loading, [input, loading]);

  useEffect(() => {
    if (typeof window === "undefined") return;
    try {
      const raw = window.localStorage.getItem(NAVIGATOR_PREFERENCE_KEY);
      if (!raw) return;
      const parsed = JSON.parse(raw) as Partial<NavigatorPreference>;
      const nextPreference: NavigatorPreference = {
        hasInteracted: parsed.hasInteracted === true,
        autoOpen: parsed.autoOpen === true,
      };
      setNavigatorPreference(nextPreference);

      if (nextPreference.hasInteracted && nextPreference.autoOpen) {
        setOpen(true);
      }
    } catch {
      // keep defaults
    }
  }, []);

  const persistNavigatorPreference = useCallback((next: NavigatorPreference) => {
    setNavigatorPreference(next);
    if (typeof window !== "undefined") {
      window.localStorage.setItem(NAVIGATOR_PREFERENCE_KEY, JSON.stringify(next));
    }
  }, []);

  useEffect(() => {
    const loadConsent = () => {
      if (typeof window === "undefined") {
        return;
      }

      try {
        const raw = window.localStorage.getItem(clientConsentKeys.storage);
        if (!raw) {
          return;
        }

        const parsed = JSON.parse(raw) as Partial<ConsentState>;
        setConsent((prev) => ({
          userId: typeof parsed.userId === "string" && parsed.userId ? parsed.userId : prev.userId,
          analytics: parsed.analytics !== false,
          training: parsed.training === true,
        }));
      } catch {
        // keep defaults
      }
    };

    const onUpdate = (event: Event) => {
      const custom = event as CustomEvent<ConsentState>;
      if (!custom.detail) return;
      setConsent({
        userId: custom.detail.userId || sessionId,
        analytics: custom.detail.analytics !== false,
        training: custom.detail.training === true,
      });
    };

    loadConsent();
    window.addEventListener(clientConsentKeys.event, onUpdate as EventListener);
    return () => {
      window.removeEventListener(clientConsentKeys.event, onUpdate as EventListener);
    };
  }, [sessionId]);

  const trackEvent = useCallback(async (eventName: string, metadata?: Record<string, unknown>) => {
    try {
      await fetch("/api/ai/behavior/track", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          event: eventName,
          source: "ai_navigator",
          userId: consent.userId || sessionId,
          metadata: {
            path: pathname,
            ...metadata,
          },
          consent: {
            analytics: consent.analytics !== false,
            training: consent.training === true,
          },
        }),
      });
    } catch {
      // Non-blocking analytics
    }
  }, [consent.analytics, consent.training, consent.userId, pathname, sessionId]);

  const sendPrompt = useCallback(async (prompt: string) => {
    const message = prompt.trim();
    if (!message) return;

    const userMessage: ChatMessage = {
      id: `user-${Date.now()}`,
      role: "user",
      content: message,
    };
    setMessages((prev) => [...prev, userMessage]);
    setInput("");
    setLoading(true);

    await trackEvent("navigator_prompt_sent", {
      prompt_length: message.length,
    });

    try {
      const response = await fetch("/api/ai/navigator", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          message,
          sessionId,
          userId: consent.userId || sessionId,
          currentPath: pathname,
          consent: {
            analytics: consent.analytics !== false,
            training: consent.training === true,
          },
        }),
      });

      const payload = await response.json();
      if (!response.ok || !payload.ok) {
        throw new Error(payload.error || "Navigator request failed");
      }

      const assistantMessage: ChatMessage = {
        id: `assistant-${Date.now()}`,
        role: "assistant",
        content: payload.response,
        suggestions: Array.isArray(payload.suggestions) ? payload.suggestions : [],
      };
      setMessages((prev) => [...prev, assistantMessage]);
    } catch {
      setMessages((prev) => [
        ...prev,
        {
          id: `assistant-error-${Date.now()}`,
          role: "assistant",
          content:
            "I hit a temporary issue. Try /, /ai-hub, /intelligence, or /pricing while I recover.",
        },
      ]);
    } finally {
      setLoading(false);
    }
  }, [consent.analytics, consent.training, consent.userId, pathname, sessionId, trackEvent]);

  useEffect(() => {
    if (typeof window === "undefined") {
      return;
    }

    const onIntroComplete = (event: Event) => {
      const custom = event as CustomEvent<IntroCompleteDetail>;
      const detail = custom.detail ?? {};
      if (detail.autoOpenNavigator === false) {
        return;
      }

      if (!navigatorPreference.hasInteracted || !navigatorPreference.autoOpen) {
        return;
      }

      setPriorityDock(true);
      setOpen(true);

      const introPrompt = detail.prompt?.trim();
      if (introPrompt) {
        setPendingPrompt(introPrompt);
      }
    };

    window.addEventListener(INTRO_COMPLETE_EVENT, onIntroComplete as EventListener);
    return () => {
      window.removeEventListener(INTRO_COMPLETE_EVENT, onIntroComplete as EventListener);
    };
  }, [navigatorPreference.autoOpen, navigatorPreference.hasInteracted]);

  useEffect(() => {
    if (!open || loading || !pendingPrompt) {
      return;
    }

    const nextPrompt = pendingPrompt;
    setPendingPrompt(null);
    void sendPrompt(nextPrompt);
  }, [loading, open, pendingPrompt, sendPrompt]);

  return (
    <div className={`fixed right-3 sm:right-4 z-[80] ${priorityDock ? "top-4" : "bottom-[max(0.75rem,env(safe-area-inset-bottom))] max-[420px]:bottom-[max(0.5rem,env(safe-area-inset-bottom))]"}`}>
      {open && (
        <div className={`${priorityDock ? "mt-3" : "mb-3"} w-[min(94vw,400px)] overflow-hidden rounded-xl border border-cyan-500/35 bg-[rgba(8,11,16,0.9)] shadow-[0_14px_40px_rgba(0,0,0,0.55),0_0_20px_rgba(6,182,212,0.24)] backdrop-blur-md`}>
          <div className="flex items-center justify-between border-b border-cyan-500/20 px-3 py-2">
            <div>
              <div className="text-xs font-bold uppercase tracking-wider text-cyan-300">Site Navigator</div>
              <div className="text-[10px] text-cyan-100/70">
                {priorityDock ? "AI concierge funnel is active" : "AI guide for new users"}
              </div>
            </div>
            <button
              type="button"
              className="inline-flex h-8 w-8 items-center justify-center rounded-full bg-red-500/85 text-white shadow-sm hover:bg-red-500 transition-colors motion-reduce:transition-none focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-red-300/70"
              aria-label="Close navigator"
              onClick={() => {
                setOpen(false);
                persistNavigatorPreference({ hasInteracted: true, autoOpen: false });
              }}
            >
              <X className="h-5 w-5" />
            </button>
          </div>

          <div className="max-h-[52vh] space-y-2 overflow-y-auto px-3 py-3">
            {messages.map((msg) => (
              <div key={msg.id} className={msg.role === "user" ? "text-right" : "text-left"}>
                <div
                  className={`inline-block max-w-[90%] rounded-lg px-3 py-2 text-xs leading-relaxed ${
                    msg.role === "user"
                      ? "border border-emerald-500/40 bg-emerald-500/20 text-emerald-100"
                      : "border border-cyan-500/30 bg-cyan-500/10 text-cyan-100"
                  }`}
                >
                  {msg.content}
                </div>
                {msg.suggestions && msg.suggestions.length > 0 && (
                  <div className="mt-2 flex flex-wrap gap-1.5">
                    {msg.suggestions.slice(0, 4).map((item) => (
                      <Link
                        key={`${msg.id}-${item.path}`}
                        href={item.path}
                        onClick={() => {
                          void trackEvent("navigator_suggestion_clicked", {
                            target_path: item.path,
                            target_title: item.title,
                          });
                        }}
                        className="rounded border border-cyan-400/30 bg-black/40 px-2 py-1 text-[10px] text-cyan-100 hover:border-cyan-300 hover:bg-cyan-500/20 transition-colors motion-reduce:transition-none"
                      >
                        {item.title}
                      </Link>
                    ))}
                  </div>
                )}
              </div>
            ))}

            {loading && (
              <div className="text-xs text-cyan-200/70">Thinking…</div>
            )}
          </div>

          <div className="border-t border-cyan-500/20 px-3 py-2">
            <div className="mb-2 flex flex-wrap gap-1">
              {STARTER_PROMPTS.map((prompt) => (
                <button
                  key={prompt}
                  type="button"
                  className="rounded border border-cyan-500/20 bg-cyan-500/5 px-2 py-1 text-[10px] text-cyan-100/80 hover:bg-cyan-500/15 transition-colors motion-reduce:transition-none"
                  onClick={() => {
                    void sendPrompt(prompt);
                  }}
                >
                  {prompt}
                </button>
              ))}
            </div>
            <div className="flex items-center gap-2">
              <input
                type="text"
                value={input}
                onChange={(event) => setInput(event.target.value)}
                onKeyDown={(event) => {
                  if (event.key === "Enter" && canSend) {
                    event.preventDefault();
                    void sendPrompt(input);
                  }
                }}
                placeholder="Ask where to go…"
                className="w-full rounded border border-cyan-500/30 bg-black/60 px-2.5 py-2 text-xs text-cyan-100 outline-none placeholder:text-cyan-200/40"
              />
              <button
                type="button"
                onClick={() => {
                  void sendPrompt(input);
                }}
                disabled={!canSend}
                className="rounded border border-cyan-400/40 bg-cyan-500/20 p-2 text-cyan-100 disabled:cursor-not-allowed disabled:opacity-50"
                aria-label="Send"
              >
                <Send className="h-4 w-4" />
              </button>
            </div>
          </div>
        </div>
      )}

      <button
        type="button"
        className="inline-flex items-center gap-2 rounded-full border border-cyan-400/50 bg-cyan-500/25 px-4 py-2.5 text-xs font-bold uppercase tracking-wider text-cyan-100 shadow-[0_0_20px_rgba(6,182,212,0.25)] hover:bg-cyan-500/35 transition-colors motion-reduce:transition-none"
        onClick={() => {
          const nextOpen = !open;
          setOpen(nextOpen);
          persistNavigatorPreference({ hasInteracted: true, autoOpen: nextOpen });
          void trackEvent("navigator_widget_toggled", {
            open_next: nextOpen,
          });
        }}
      >
        <MessageCircle className="h-4 w-4" />
        {open ? "Close Guide" : "Need Help?"}
      </button>
    </div>
  );
}
